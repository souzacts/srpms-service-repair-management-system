--------------------------------------------------------
-- SRPMS schema and sample data
--------------------------------------------------------

--------------------------------------------------------
-- CUSTOMER
--------------------------------------------------------
CREATE TABLE customer (
    customer_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name VARCHAR2(50) NOT NULL,
    last_name VARCHAR2(50) NOT NULL,
    phone VARCHAR2(20),
    email VARCHAR2(100),
    street VARCHAR2(100),
    city VARCHAR2(50),
    state VARCHAR2(30),
    zip_code VARCHAR2(15)
);

--------------------------------------------------------
-- EQUIPMENT_MODEL
--------------------------------------------------------
CREATE TABLE equipment_model (
    equipment_model_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    equipment_type VARCHAR2(50) NOT NULL,
    make VARCHAR2(50) NOT NULL,
    model VARCHAR2(50) NOT NULL,
    model_year NUMBER(4,0),
    engine_spec VARCHAR2(100)
);

--------------------------------------------------------
-- PART
--------------------------------------------------------
CREATE TABLE part (
    part_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    part_name VARCHAR2(100) NOT NULL,
    category VARCHAR2(50),
    sku VARCHAR2(50) NOT NULL UNIQUE,
    unit_price NUMBER(10,2) NOT NULL,
    quantity_in_stock NUMBER DEFAULT 0 NOT NULL,
    CONSTRAINT chk_part_unit_price CHECK (unit_price >= 0),
    CONSTRAINT chk_part_quantity_in_stock CHECK (quantity_in_stock >= 0)
);

--------------------------------------------------------
-- EQUIPMENT
--------------------------------------------------------
CREATE TABLE equipment (
    equipment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER NOT NULL,
    equipment_model_id NUMBER NOT NULL,
    serial_number VARCHAR2(100),
    CONSTRAINT uq_equipment_serial_number UNIQUE (serial_number),
    CONSTRAINT fk_equipment_customer FOREIGN KEY (customer_id)
        REFERENCES customer(customer_id),
    CONSTRAINT fk_equipment_model FOREIGN KEY (equipment_model_id)
        REFERENCES equipment_model(equipment_model_id)
);

--------------------------------------------------------
-- SERVICE_ORDER
--------------------------------------------------------
CREATE TABLE service_order (
    service_order_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    equipment_id NUMBER NOT NULL,
    date_opened DATE DEFAULT SYSDATE NOT NULL,
    reported_issue VARCHAR2(500),
    diagnosis_notes VARCHAR2(1000),
    labor_cost NUMBER(10,2) DEFAULT 0,
    status VARCHAR2(30) DEFAULT 'DROPPED_OFF' NOT NULL,
    date_closed DATE,
    CONSTRAINT fk_service_order_equipment FOREIGN KEY (equipment_id)
        REFERENCES equipment(equipment_id),
    CONSTRAINT chk_service_order_labor_cost CHECK (labor_cost >= 0),
    CONSTRAINT chk_service_order_status CHECK (
        status IN (
            'DROPPED_OFF',
            'AWAITING_APPROVAL',
            'DECLINED',
            'WAITING_FOR_PARTS',
            'IN_REPAIR',
            'READY_FOR_PICKUP',
            'CLOSED'
        )
    )
);

--------------------------------------------------------
-- INVOICE
--------------------------------------------------------
CREATE TABLE invoice (
    invoice_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    service_order_id NUMBER NOT NULL,
    invoice_date DATE DEFAULT SYSDATE NOT NULL,
    due_date DATE DEFAULT (SYSDATE + 30),
    subtotal NUMBER(10,2) DEFAULT 0,
    tax_amount NUMBER(10,2) DEFAULT 0,
    payment_date DATE,
    payment_status VARCHAR2(30) DEFAULT 'UNPAID',
    payment_method VARCHAR2(30),
    CONSTRAINT uq_invoice_service_order UNIQUE (service_order_id),
    CONSTRAINT fk_invoice_service_order FOREIGN KEY (service_order_id)
        REFERENCES service_order(service_order_id),
    CONSTRAINT chk_invoice_subtotal CHECK (subtotal >= 0),
    CONSTRAINT chk_invoice_tax_amount CHECK (tax_amount >= 0),
    CONSTRAINT chk_invoice_payment_status CHECK (
        payment_status IN ('UNPAID', 'PAID', 'OVERDUE')
    ),
    CONSTRAINT chk_invoice_payment_method CHECK (
        payment_method IN ('CASH', 'CARD', 'CHECK')
    )
);

--------------------------------------------------------
-- SERVICE_ORDER_PART
--------------------------------------------------------
CREATE TABLE service_order_part (
    service_order_id NUMBER NOT NULL,
    part_id NUMBER NOT NULL,
    quantity_used NUMBER NOT NULL,
    unit_price_at_use NUMBER(10,2) NOT NULL,
    CONSTRAINT pk_service_order_part PRIMARY KEY (service_order_id, part_id),
    CONSTRAINT fk_sop_service_order FOREIGN KEY (service_order_id)
        REFERENCES service_order(service_order_id),
    CONSTRAINT fk_sop_part FOREIGN KEY (part_id)
        REFERENCES part(part_id),
    CONSTRAINT chk_sop_quantity_used CHECK (quantity_used > 0),
    CONSTRAINT chk_sop_unit_price_at_use CHECK (unit_price_at_use >= 0)
);

--------------------------------------------------------
-- PART_FITMENT
--------------------------------------------------------
CREATE TABLE part_fitment (
    equipment_model_id NUMBER NOT NULL,
    part_id NUMBER NOT NULL,
    notes VARCHAR2(500),
    CONSTRAINT pk_part_fitment PRIMARY KEY (equipment_model_id, part_id),
    CONSTRAINT fk_pf_equipment_model FOREIGN KEY (equipment_model_id)
        REFERENCES equipment_model(equipment_model_id),
    CONSTRAINT fk_pf_part FOREIGN KEY (part_id)
        REFERENCES part(part_id)
);

--------------------------------------------------------
-- INDEXES
--------------------------------------------------------
CREATE INDEX idx_equipment_customer ON equipment(customer_id);
CREATE INDEX idx_equipment_model ON equipment(equipment_model_id);
CREATE INDEX idx_service_order_equipment ON service_order(equipment_id);
CREATE INDEX idx_service_order_part_part ON service_order_part(part_id);
CREATE INDEX idx_part_fitment_model ON part_fitment(equipment_model_id);
CREATE INDEX idx_part_fitment_part ON part_fitment(part_id);

--------------------------------------------------------
-- TRIGGERS
--------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_service_order_reserve_parts
BEFORE UPDATE OF status ON service_order
FOR EACH ROW
DECLARE
    v_available_qty part.quantity_in_stock%TYPE;
    v_missing_parts NUMBER := 0;
BEGIN
    IF :OLD.status = 'AWAITING_APPROVAL'
       AND :NEW.status IN ('WAITING_FOR_PARTS', 'IN_REPAIR') THEN

        FOR r IN (
            SELECT part_id, quantity_used
            FROM service_order_part
            WHERE service_order_id = :OLD.service_order_id
        )
        LOOP
            SELECT quantity_in_stock
            INTO v_available_qty
            FROM part
            WHERE part_id = r.part_id
            FOR UPDATE;

            IF v_available_qty >= r.quantity_used THEN
                UPDATE part
                SET quantity_in_stock = quantity_in_stock - r.quantity_used
                WHERE part_id = r.part_id;
            ELSE
                v_missing_parts := 1;
            END IF;
        END LOOP;

        IF v_missing_parts = 1 THEN
            :NEW.status := 'WAITING_FOR_PARTS';
        ELSE
            :NEW.status := 'IN_REPAIR';
        END IF;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER trg_create_invoice_ready_pickup
AFTER UPDATE OF status ON service_order
FOR EACH ROW
DECLARE
    v_subtotal NUMBER(10,2);
    v_tax_amount NUMBER(10,2);
    v_count NUMBER;
BEGIN
    IF :NEW.status = 'READY_FOR_PICKUP'
       AND NVL(:OLD.status, 'X') <> 'READY_FOR_PICKUP' THEN

        SELECT COUNT(*)
        INTO v_count
        FROM invoice
        WHERE service_order_id = :NEW.service_order_id;

        IF v_count = 0 THEN
            SELECT NVL(:NEW.labor_cost, 0) +
                   NVL(SUM(sop.quantity_used * sop.unit_price_at_use), 0)
            INTO v_subtotal
            FROM service_order_part sop
            WHERE sop.service_order_id = :NEW.service_order_id;

            v_tax_amount := ROUND(v_subtotal * 0.07, 2);

            INSERT INTO invoice (
                service_order_id,
                invoice_date,
                due_date,
                subtotal,
                tax_amount,
                payment_status
            )
            VALUES (
                :NEW.service_order_id,
                SYSDATE,
                SYSDATE + 30,
                v_subtotal,
                v_tax_amount,
                'UNPAID'
            );
        END IF;
    END IF;
END;
/

